package com.dapurtempoedoeloe.model;

public class ResponseModelOrder {

    String kode, pesan, id_menu, id_meja;

    public String getKode() {
        return kode;
    }
    public void setKode(String kode) {
        this.kode = kode;
    }
    public String getPesan() {
        return pesan;
    }
    public void setPesan(String pesan) {
        this.pesan = pesan;
    }
    public String getId_menu() {
        return id_menu;
    }
    public void setId_menu(String id_meja) {
        this.id_menu = id_menu;
    }
    public String getId_meja() {
        return id_meja;
    }
    public void setId_meja(String id_meja) {
        this.id_meja = id_meja;
    }
}
